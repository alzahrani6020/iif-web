  function detectLang() {
    try {
      var stored = localStorage.getItem('iif-lang');
      if (stored && codes.indexOf(stored) >= 0) return stored;
      var nav = navigator.language || navigator.userLanguage || '';
      var base = nav.split('-')[0].toLowerCase();
      return browserMap[base] || browserMap[nav.slice(0, 5).toLowerCase()] || 'en';
    } catch (e) { return 'en'; }
  }

  function applyLang(lang) {
    lang = (typeof lang === 'string' ? lang : '').trim().toLowerCase();
    if (!lang || codes.indexOf(lang) < 0) lang = 'en';
    var t = T[lang] || T.en;
    var fallback = T.en;
    var info = L[lang] || L.en;
    var html = document.documentElement;
    html.setAttribute('lang', lang);
    html.setAttribute('dir', info.dir);
    html.setAttribute('data-lang', lang);
    try { localStorage.setItem('iif-lang', lang); } catch (e) {}
    document.querySelectorAll('[data-i18n]').forEach(function(el) {
      var key = el.getAttribute('data-i18n');
      var val = (t && t[key] != null) ? t[key] : (fallback && fallback[key]);
      if (val != null) {
        if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') el.placeholder = val;
        else el.textContent = val;
      }
    });
    var picker = document.getElementById('iif-lang-picker');
    if (picker) picker.value = lang;
  }


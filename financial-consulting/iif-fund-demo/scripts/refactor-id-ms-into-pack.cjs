'use strict';
/**
 * Extracts service keys from T.id / T.ms into pack-data-c-id-ms.cjs
 * and rewrites i18n.js to use extend(extend(def, EXT_SVC.xx || {}), { ...rest }).
 */
const fs = require('fs');
const path = require('path');

const I18N = path.join(__dirname, '../i18n.js');
const OUT_PACK = path.join(__dirname, '../pack-data-c-id-ms.cjs');

const SERVICE_KEYS = Object.keys(require('../pack-data-c.cjs').vi).sort();

function extractBraceObject(src, needle) {
  const start = src.indexOf(needle);
  if (start < 0) throw new Error('Needle not found: ' + needle);
  let i = start + needle.length - 1;
  if (src[i] !== '{') throw new Error('Expected { after needle');
  let depth = 0;
  let inStr = null;
  let strChar = null;
  let esc = false;
  for (; i < src.length; i++) {
    const c = src[i];
    if (inStr) {
      if (esc) {
        esc = false;
        continue;
      }
      if (c === '\\' && (strChar === "'" || strChar === '"')) {
        esc = true;
        continue;
      }
      if (c === strChar) {
        inStr = null;
        strChar = null;
      }
      continue;
    }
    if (c === "'" || c === '"') {
      inStr = true;
      strChar = c;
      continue;
    }
    if (c === '{') depth++;
    if (c === '}') {
      depth--;
      if (depth === 0) {
        let end = i + 1;
        while (end < src.length && /\s/.test(src[end])) end++;
        if (src.slice(end, end + 2) === ');') end += 2;
        return {
          fullStart: start,
          fullEnd: end,
          inner: src.slice(start + needle.length - 1, i + 1)
        };
      }
    }
  }
  throw new Error('Unclosed brace');
}

function quoteJsString(s) {
  return (
    "'" +
    String(s)
      .replace(/\\/g, '\\\\')
      .replace(/'/g, "\\'")
      .replace(/\r\n/g, '\n')
      .replace(/\n/g, '\\n') +
    "'"
  );
}

function serializeBlock(lang, obj) {
  const keys = Object.keys(obj);
  const lines = [];
  lines.push('  T.' + lang + ' = extend(extend(def, EXT_SVC.' + lang + ' || {}), {');
  for (let i = 0; i < keys.length; i++) {
    const k = keys[i];
    const v = obj[k];
    if (typeof v !== 'string') {
      throw new Error('Non-string value ' + lang + '.' + k + ' ' + typeof v);
    }
    lines.push('    ' + k + ': ' + quoteJsString(v) + (i < keys.length - 1 ? ',' : ''));
  }
  lines.push('  });');
  return lines.join('\n');
}

function main() {
  let text = fs.readFileSync(I18N, 'utf8');
  const packOut = { id: {}, ms: {} };

  for (const lang of ['id', 'ms']) {
    const needle = '  T.' + lang + ' = extend(def, {';
    const { fullStart, fullEnd, inner } = extractBraceObject(text, needle);
    const o = new Function('return ' + inner)();
    const rest = Object.assign({}, o);
    for (let k = 0; k < SERVICE_KEYS.length; k++) {
      const key = SERVICE_KEYS[k];
      if (Object.prototype.hasOwnProperty.call(o, key)) {
        packOut[lang][key] = o[key];
        delete rest[key];
      }
    }
    const replacement = serializeBlock(lang, rest);
    text = text.slice(0, fullStart) + replacement + text.slice(fullEnd);
  }

  const packBody =
    '/* eslint-disable max-len */\n' +
    '/* Auto-generated by scripts/refactor-id-ms-into-pack.cjs */\n' +
    'module.exports = ' +
    JSON.stringify(packOut, null, 2) +
    ';\n';
  fs.writeFileSync(OUT_PACK, packBody, 'utf8');
  fs.writeFileSync(I18N, text, 'utf8');
  console.log('Wrote', path.relative(process.cwd(), OUT_PACK));
  console.log('Updated', path.relative(process.cwd(), I18N));
}

main();

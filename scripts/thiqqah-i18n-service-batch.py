def js_escape(s):
    return s.replace("\\", "\\\\").replace('"', '\\"')


def format_translation_fragment(mapping):
    lines = []
    for ar in sorted(mapping.keys()):
        en = mapping[ar]
        lines.append(f'      "{js_escape(ar)}": "{js_escape(en)}",')
    return "\n".join(lines)


